//Escreva um código que receba um número (1-7) e devolva o dia da semana correspondente.

var dia = 8

switch(dia){
    case dia = 1:
        console.log("É segunda-feira");
        break;
    case dia = 2:
        console.log("É terça-feira");
        break;
    case dia = 3:
        console.log("É quarta-feira");
        break;
    case dia = 4:
        console.log("É quinta-feira");
        break;    
    case dia = 5:
        console.log("É sexta-feira");
        break;
    case dia = 6:  
        console.log("É sábado");
        break;
    case dia = 7:
        console.log ("É domingo");   
        break;
    default:
        console.log("Não corresponde a nenhum dia da semana")     
}